// Build-time generator for repo social-preview image URLs.
//
// GitHub serves a repo's UPLOADED social preview from an unpredictable
// repository-images.githubusercontent.com URL that can't be derived client-side
// (and the repo HTML page is CORS-blocked in the browser). So we resolve it here,
// server-side at build time: fetch each repo's page, read its og:image, and keep
// only real custom previews. The result is written to a generated TS file that the
// app imports. New repos with an uploaded preview "just work" on the next build —
// no manual map editing.
//
// Usage: node scripts/fetch-social-previews.mjs
// Runs automatically via the "prebuild" npm script.

import { writeFile, mkdir } from 'node:fs/promises';
import { dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const GH_USER = 'MacroMaster101';
const OUT_PATH = fileURLToPath(new URL('../src/data/socialPreviews.ts', import.meta.url));

// Optional token (raises rate limit). Read-only, public — same one the app uses.
const token = process.env.VITE_GITHUB_TOKEN || process.env.GITHUB_TOKEN;
const headers = {
  'User-Agent': 'portfolio-build-script',
  ...(token ? { Authorization: `Bearer ${token}` } : {}),
};

// Pull the og:image out of a repo's HTML. A custom upload lives on
// repository-images.githubusercontent.com; the generated graph card lives on
// opengraph.githubassets.com (which we skip — the app falls back to it anyway).
function extractCustomPreview(html) {
  const match = html.match(/<meta[^>]+property=["']og:image["'][^>]+content=["']([^"']+)["']/i);
  if (!match) return null;
  const url = match[1];
  return url.includes('repository-images.githubusercontent.com') ? url : null;
}

async function main() {
  console.log(`[social-previews] fetching repos for ${GH_USER}…`);

  const reposRes = await fetch(
    `https://api.github.com/users/${GH_USER}/repos?per_page=100&type=public`,
    { headers }
  );
  if (!reposRes.ok) {
    console.warn(`[social-previews] repo list fetch failed (${reposRes.status}); leaving existing file untouched.`);
    return;
  }
  const repos = await reposRes.json();
  if (!Array.isArray(repos)) {
    console.warn('[social-previews] unexpected API response; leaving existing file untouched.');
    return;
  }

  const entries = {};
  await Promise.all(
    repos.map(async (repo) => {
      try {
        const pageRes = await fetch(`https://github.com/${GH_USER}/${repo.name}`, {
          headers: { 'User-Agent': 'portfolio-build-script' },
        });
        if (!pageRes.ok) return;
        const html = await pageRes.text();
        const preview = extractCustomPreview(html);
        if (preview) entries[repo.name.toLowerCase()] = preview;
      } catch {
        /* skip repos that fail to fetch */
      }
    })
  );

  const sorted = Object.fromEntries(Object.entries(entries).sort(([a], [b]) => a.localeCompare(b)));
  const count = Object.keys(sorted).length;

  const body = `// AUTO-GENERATED by scripts/fetch-social-previews.mjs — do not edit by hand.
// Maps a lowercased repo name to its uploaded GitHub social-preview image URL.
// Regenerated on every build (npm run prebuild). Repos without a custom preview are
// omitted; the app falls back to GitHub's generated graph card for those.

export const SOCIAL_PREVIEWS: Record<string, string> = ${JSON.stringify(sorted, null, 2)};
`;

  await mkdir(dirname(OUT_PATH), { recursive: true });
  await writeFile(OUT_PATH, body, 'utf8');
  console.log(`[social-previews] wrote ${count} custom preview(s) to src/data/socialPreviews.ts`);
}

main().catch((err) => {
  // Never fail the build over preview generation — the app has a graph-card fallback.
  console.warn('[social-previews] generation skipped due to error:', err?.message ?? err);
});
